  final correoController= TextEditingController();
  final contrasenaController= TextEditingController();
  Future<bool> loginCorreo() async{
    var url = 'http://35.194.16.129/service/peopleapp/usuarios/login/';
    var body = jsonEncode({ 'correo': correoController.text,'contrasena': contrasenaController.text});
    var response = await http.post(url,
     body:body );
    String respu=response.body.replaceAll(new RegExp(r'usuarios/login/'), '');
    final resp = json.decode(respu);

    if (resp['success']!=null || resp['success']!='is not true') {
      //Token is nested inside data field so it goes one deeper.
      final token = resp['estado'];
      print("succes");
      print('Response status: ${response.statusCode}');
      print('Response body: ${response.body}');
      if(token==1){
        Fluttertoast.showToast(
          msg: "Iniciando sesión",
          toastLength: Toast.LENGTH_LONG,
        );
        return true;
      }
      else{
        Fluttertoast.showToast(
          msg: resp['mensaje'],
          toastLength: Toast.LENGTH_LONG,
        );
        return false;
      }
    }
    print('Response status: ${response.statusCode}');
    print('Response body: ${response.body}');
    
    return false;
  }
